/**
 * Returns mock cards matching query for the given TCG.
 * Simulates the behavior a real adapter (e.g. Scrydex) would have.
 */
export const getMockCards = (query: string, tcg: string): NormalizedCard[] => {
  const cards = ALL_MOCK_CARDS[tcg] ?? [];
  const lowerQuery = query.toLowerCase();

  return cards.filter((card) => card.name.toLowerCase().includes(lowerQuery));
};
